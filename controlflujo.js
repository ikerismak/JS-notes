// Control de Flujo:

// El control de flujo se utiliza para dirigir la ejecución de un programa en función de condiciones y eventos.
// Permite tomar decisiones y ejecutar diferentes bloques de código en función de las condiciones específicas.



// if...else:
// La declaración if se usa para ejecutar un bloque de código si una condición es verdadera.
// La declaración else se usa para ejecutar un bloque de código si la condición es falsa.


if (condicion) {
    // Bloque de código si la condición es verdadera
  } else {
    // Bloque de código si la condición es falsa
  }



//   Switch:
//   La declaración switch permite evaluar una expresión y ejecutar diferentes bloques de código en función de los valores coincidentes de esa expresión


switch (expresion) {
    case valor1:
      // Bloque de código si expresion === valor1
      break;
    case valor2:
      // Bloque de código si expresion === valor2
      break;
    default:
      // Bloque de código si no se encuentra ninguna coincidencia
  }

