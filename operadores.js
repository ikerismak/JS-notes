// Operadores Aritméticos:
// + : Suma.
// - : Resta.
// * : Multiplicación.
// / : División.
// % : Módulo (resto de la división).
// ++ : Incremento (aumenta el valor en 1).
// -- : Decremento (disminuye el valor en 1).

let a = 5;
let b = 2;
let suma = a + b; // 7
let resta = a - b; // 3
let multiplicacion = a * b; // 10
let division = a / b; // 2.5
let modulo = a % b; // 1


// Operadores de Asignación:
// = : Asignación.
// += : Asignación con suma.
// -= : Asignación con resta.
// *= : Asignación con multiplicación.
// /= : Asignación con división.

let x = 10;
x += 5; // x ahora es igual a 15

// Operadores de Comparación:
// == : Igualdad (compara el valor).
// === : Igualdad estricta (compara el valor y el tipo de datos).
// != : Desigualdad (compara el valor).
// !== : Desigualdad estricta (compara el valor y el tipo de datos).
// > : Mayor que.
// < : Menor que.
// >= : Mayor o igual que.
// <= : Menor o igual que.

let num1 = 10;
let num2 = '10';

num1 == num2; // true (compara el valor)
num1 === num2; // false (compara el valor y el tipo de datos)


// Operadores Lógicos:
// && : AND lógico.
// || : OR lógico.
// ! : NOT lógico.


let a = true;
let b = false;

a && b; // false
a || b; // true
!a; // false
